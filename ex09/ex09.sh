#!/bin/bash
if [[ $1 == "-e" || $1 == "--email" ]]
then
	grep '[[:alnum:]_]\+[@]\{1\}[[:alnum:]_]\+[\.]\{1\}[[:alpha:]]\{2,4\}' $2
fi

if [[ $1 == "-u" || $1 == "--url" ]]
then
	grep '^\(http\|https\)\{1\}:\/\/\([[:alnum:]]\+\.\)\+\([[:alpha:]]\+\)\{1\}\(\(\/\{1\}[[:alnum:]]\+\)\{1\}\(\.\{1\}[[:alnum:]]\+\)*\)*\/\?$' $2	
fi

